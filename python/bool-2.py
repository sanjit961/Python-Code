#Bool evaluation of values and variables:
#The bool() function allows you to evaluate any
#value, and give True or False in return,,,
#Example:
#Evaluate a string and a number:
print(bool("Hello"))
print(bool(15))

#Evaluate Two Variables:
x = "Hello"
y = 15
print(bool(x))
print(bool(y))

#More Examples:
bool("abc")
bool(123)
bool(["apple", "cherry", "banana"])
