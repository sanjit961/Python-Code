#REPLACE FUNCTION IN PYTHON
course = 'Python for Beginners'
print(course.replace('Beginners', 'Absolute Beginners'))
#This is case sensitive

#More example:
print(course.replace('P', 'S'))

#Code to check whether the particular string is present in the variable
print('Python' in course) #Displays in either in True or False

#More on this:
print('python' in course) #Case-Sensitive

